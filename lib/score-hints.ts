export function getScoreHint(key: ScoreHintKey): ScoreHint {
  return SCORE_HINTS[key];
}

export function needsImprovement(
  key: ScoreHintKey,
  score: number
): boolean {
  if (key === "harassment") return score >= 40;
  return score < 60;
}
